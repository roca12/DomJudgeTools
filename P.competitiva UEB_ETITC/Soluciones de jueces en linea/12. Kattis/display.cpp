#include <iostream>
#include <vector>

using namespace std;

void addspace(vector<string>& v) {
    for(auto& s : v) {
        s += "  ";
    }
}

void removespace(vector<string>& v) {
    for(auto& s : v) {
        s.pop_back();
        s.pop_back();
    }
}

void print(vector<string>& v) {
    for(auto s : v) {
        cout << s << endl;
    }
    cout << endl << endl;
}

void addchar(char c, vector<string>& v) {
    if(c == '0') {
        v[0] += "+---+";
        v[1] += "|   |";
        v[2] += "|   |";
        v[3] += "+   +";
        v[4] += "|   |";
        v[5] += "|   |";
        v[6] += "+---+";
    }
    if(c == '1') {
        v[0] += "    +";
        v[1] += "    |";
        v[2] += "    |";
        v[3] += "    +";
        v[4] += "    |";
        v[5] += "    |";
        v[6] += "    +";
    }
    if(c == '2') {
        v[0] += "+---+";
        v[1] += "    |";
        v[2] += "    |";
        v[3] += "+---+";
        v[4] += "|    ";
        v[5] += "|    ";
        v[6] += "+---+";
    }
    if(c == '3') {
        v[0] += "+---+";
        v[1] += "    |";
        v[2] += "    |";
        v[3] += "+---+";
        v[4] += "    |";
        v[5] += "    |";
        v[6] += "+---+";
    }
    if(c == '4') {
        v[0] += "+   +";
        v[1] += "|   |";
        v[2] += "|   |";
        v[3] += "+---+";
        v[4] += "    |";
        v[5] += "    |";
        v[6] += "    +";
    }
    if(c == '5') {
        v[0] += "+---+";
        v[1] += "|    ";
        v[2] += "|    ";
        v[3] += "+---+";
        v[4] += "    |";
        v[5] += "    |";
        v[6] += "+---+";
    }
    if(c == '6') {
        v[0] += "+---+";
        v[1] += "|    ";
        v[2] += "|    ";
        v[3] += "+---+";
        v[4] += "|   |";
        v[5] += "|   |";
        v[6] += "+---+";
    }
    if(c == '7') {
        v[0] += "+---+";
        v[1] += "    |";
        v[2] += "    |";
        v[3] += "    +";
        v[4] += "    |";
        v[5] += "    |";
        v[6] += "    +";
    }
    if(c == '8') {
        v[0] += "+---+";
        v[1] += "|   |";
        v[2] += "|   |";
        v[3] += "+---+";
        v[4] += "|   |";
        v[5] += "|   |";
        v[6] += "+---+";
    }
    if(c == '9') {
        v[0] += "+---+";
        v[1] += "|   |";
        v[2] += "|   |";
        v[3] += "+---+";
        v[4] += "    |";
        v[5] += "    |";
        v[6] += "+---+";
    }
    if(c == ':') {
        v[0] += " ";
        v[1] += " ";
        v[2] += "o";
        v[3] += " ";
        v[4] += "o";
        v[5] += " ";
        v[6] += " ";
    }
}

int main() {
    string s;
    while(getline(cin, s) && s != "end") {
        vector<string> v;
        v.resize(7);
        for(auto c : s) {
            addchar(c, v);
            addspace(v);
        }
        removespace(v);
        print(v);
    }
    cout << "end" << endl;
}
